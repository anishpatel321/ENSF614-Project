package com.anish.FlightApp;

/**
 * 
 * Ticket object that stores all of the ticket data
 * and allows for each to be set to the instance
 * and for each to be pulled from
 * 
 */


public class Ticket {
    private String firstName;
    private String lastName;
    private String seatNumber;
    private String email;
    private int cardNumber;

    public Ticket() {
        
    }

    public Ticket(String firstName,  String lastName,String seatNumber,  String email, int cardNumber) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.seatNumber = seatNumber;
        this.email = email;
        this.cardNumber = cardNumber;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getSeatNumber() {
        return seatNumber;
    }

    public void setSeatNumber(String seatNumber) {
        this.seatNumber = seatNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getCardNumber() {
        return cardNumber;
    }

    public void setCardNumber(int cardNumber) {
        this.cardNumber = cardNumber;
    }
}